   <p>Tas merupakan sebuah wadah yang digunakan untuk menyimpan keperluan bepergian. Material pembuatan tas juga beragam seperti kulit, kain, kertas, plastik, dll. Kebutuhan akan benda yang satu ini dapat dilihat dari kegiatan yang dilalui &nbsp;sehari-hari seperti sekolah, pesta, kantor, maupun olahraga. Sehingga, kebutuhan yang berbeda membuat jenis tas yang digunakan juga berbeda. Lalu, apa saja jenis tas menurut fungsinya? Berikut ini merupakan ulasan tentang jenis tas untuk ke pesta, kantor, maupun olahraga.</p>
   <p>
      <strong>Tas Pesta</strong>
   </p>
   <p>Merupakan tas yang didesain khusus untuk ke pesta dengan mode dan pernak-pernik yang unik. Berikut ini merupakan rekomendasi tas khusus ke pesta:</p>
   <ol>
      <li>Baguette Bag
         <br />
         Merupakan tas yang didesain khusus dengan tali tas yang dapat digenggam sehingga memudahkan penggunanya untuk menggunakannya. Seperti fungsinya, tas ini digunakan untuk menyimpan berbagai peralatan yang lumrah dibawa ke pesta layaknya dompet, kosmetik, handphone, dan lain-lain.
      </li>
      <li>
         <p>Clutch Bag.
            <br />
            Clutch bag merupakan tas yang digunakan dalam acara formal dengan desain yang unik dan cantik. Namun, pilihlah desain clutch bag yang netral agar dapat disesuaikan dengan dress apa saja.
         </p>
      </li>
      <li>Envelope Bag
         <br />
         Sesuai dengan namanya, envelope bag merupakan jenis tas berbentuk amplop yang digunakan dengan cara digenggam. So, tas ini terkesan simpel dan mudah dibawa-bawa.
      </li>
      <li>
         <p>Flap Bag
            <br />
            Merupakan tas mungil dengan motif kotak-kotak timbul yang sangat sesuai dibawa untuk acara pesta.
         </p>
      </li>
      <li>Jelly Kelly Bag
         <br />
         Nama tas ini diambil dari desainnya yang berbentuk jelly yang terbuat dari bahan karet sintetis dengan kombinasi warna warni yang ceria.
         <br />
         &nbsp;
      </li>
      <li>Minaudiere
         <br />
         Merupakan jenis tas yang dijahit dengan berbagai motif diamond yang merupakan ciri khas tas ini.
         <br />
         &nbsp;
      </li>
      <li>
         Wristlet Bag
         <p>Sama seperti clutch bag, wristlet bag merupakan tas berukuran mini dengan motif cantik. Namun, letak perbedaannya adalah wristlet bag memberikan tali tipis bermaterial rantai yang digunakan untuk memudahkan penggunanya.</p>
      </li>
   </ol>
   <p>
      <strong>Tas Kantor</strong>
   </p>
   <p>Bagi kamu yang sedang kebingungan memilih rekomendasi tas khusus ke kantor, nah berikut ini adalah rekomendasinya.</p>
   <ol>
      <li>Attache Bag
         <br />
         Tas berbentuk pipih ini sangat cocok digunakan untuk aktivitas kantoran yang dapat menunjang penyimpanan dan keamanan data-data.
      </li>
      <li>Bucket Pouch
         <br />
         Bagi kamu yang ingin tampil simpel ke kantor, bucket pouch merupakan rekomendasi yang tepat dengan desainnya yang unik. Tas ini tidak menyediakan resleting, hanya saja terdapat tali yang digunakan untuk membuka dan menutup tas dengan cara menariknya.
      </li>
      <li>Doctor&rsquo;s Bag
         <br />
         Sebenarnya doctor&rsquo;s bag merupakan tas yang digunakan para dokter sejak tahun 1800an yang digunakan sebagai penyimpanan peralatan medis. Seiring perkembangan fashion membuat tas jenis ini dapat digunakan oleh siapapun yang dilengkapi dengan mode dan berbagai motif nan elegan.
      </li>
      <li>Field Bag
         <br />
         Jenis tas ini sudah ada sejak lama yang pada awalnya digunakan untuk membawa peralatan kerja. Hingga kini pun tas ini tetap menjadi tas populer yang fleksibel dengan desain kekinian.
      </li>
      <li>Hobo Bag
         <br />
         Bagi kamu yang menginginkan tas yang dapat diatur penggunaannya, tas ini merupakan rekomendasi yang tepat. Hobo bag menyediakan dua tali yaitu tali panjang dan pendek yang dapat disesuaikan penggunaannya.
      </li>
      <li>Messenger Bag
         <br />
         Messenger bag merupakan jenis tas yang sudah ada sejak zaman dahulu yang digunakan untuk membawa peralatan pos. Namun, seiring perkembangan zaman, tas ini tersedia dalam berbagai mode, warna, dan corak yang kekinian.
      </li>
      <li>Rucksack
         <br />
         Mode tas ini terbilang umum digunakan bagi siapapun. Rucksack atau tas punggung memberikan ruang penyimpanan besar yang sangat cocok digunakan untuk menyimpan peralatan kantor seperti laptop, charger, dan data lainnya.
      </li>
      <li>Satchel Bag
         <br />
         Merupakan tas yang didesain berbentuk kotak yang dilengkapi dengan dua pegangan agar memudahkan penggunanya. Keunikan tas ini terletak pada mode yang dapat disesuaikan dengan postur tubuh penggunanya.
      </li>
      <li>Shoulder Bag
         <br />
         Bagi kamu yang ingin memiliki jenis tas kasual, tas ini merupakan rekomendasi terbaik dengan memberi kesan santai. Tali tas yang tersedia juga dapat diatur sesuai dengan kenyamanan penggunanya.
      </li>
      <li>Swagger Bag
         <br />
         Merupakan tas klasik dengan sentuhan lempeng besi yang membuatnya tampak lebih menarik.
      </li>
      <li>Tote bag
         <br />
         Tas lengan berukuran besar ini terbuat dari berbagai jenis material seperti kain, kulit, maupun nilon. Dengan desainnya yang kreatif mampu membuat tas ini sangat diminati hingga saat ini.
      </li>
   </ol>
